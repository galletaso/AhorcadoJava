    private void dibujaImagen(int numeroFallos){
        
        int anchoPanel = panel.getWidth();
        int altoPanel = panel.getHeight();
        
        //hacemos variables URL con cada direccion de cada imagen del ahorcado
        URL imagen0 = null;
            imagen0 = getClass().getResource("/imagenes/ahorcado_0.png");
        
        URL imagen1 = getClass().getResource("/imagenes/ahorcado_1.png");
        URL imagen2 = getClass().getResource("/imagenes/ahorcado_2.png");
        URL imagen3 = getClass().getResource("/imagenes/ahorcado_3.png");
        URL imagen4 = getClass().getResource("/imagenes/ahorcado_4.png");
        URL imagen5 = getClass().getResource("/imagenes/ahorcado_5.png");
        URL imagen6 = getClass().getResource("/imagenes/ahorcado_fin.png");
       
        //creamos ImageIcons con esas variables que contienen las direcciones y las escalamos al tamaño del panel usando las variables que contienen el tamaño del panel y SCALE_DEFAULT
        ImageIcon hanged0 = null;
            hanged0 = new ImageIcon(new ImageIcon(imagen0).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT)); //la doble conversión a ImageIcon es porque estamos usando métodos (como getScaledInstance()) que son de Image, no de ImageIcon
            
        ImageIcon hanged1 = new ImageIcon(new ImageIcon(imagen1).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        ImageIcon hanged2 = new ImageIcon(new ImageIcon(imagen2).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        ImageIcon hanged3 = new ImageIcon(new ImageIcon(imagen3).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        ImageIcon hanged4 = new ImageIcon(new ImageIcon(imagen4).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        ImageIcon hanged5 = new ImageIcon(new ImageIcon(imagen5).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        ImageIcon hanged6 = new ImageIcon(new ImageIcon(imagen6).getImage().getScaledInstance(anchoPanel, altoPanel, Image.SCALE_DEFAULT));
        
        switch(numeroFallos){
            case 0:
                panel.setIcon(hanged0);
            break;
            
            case 1:
                panel.setIcon(hanged1);
            break;
            
            case 2:
                panel.setIcon(hanged2);
            break;
            
            case 3:
                panel.setIcon(hanged3);
            break;
            
            case 4:
                panel.setIcon(hanged4);
            break;
            
            case 5:
                panel.setIcon(hanged5);
            break;
            
            case 6:
                panel.setIcon(hanged6);
            break;
            
        }
    }

