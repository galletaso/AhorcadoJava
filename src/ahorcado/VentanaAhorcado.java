    private void chequeaLetra(String letra){
        letra = letra.toUpperCase();//para convertir la letra a mayúscula, en este caso es innecesario puesto que las letras ya están en mayúscula
        StringBuilder palabraResuelta = new StringBuilder(palabraOculta.getText());
        
        if(palabra.contains(letra)){
            for(int i=0; i<palabra.length();i++){
                if(letra.charAt(0) == palabra.charAt(i)){ //como letra es string y charAt devuelve un char, no comparamos el string letra sino sólo la posición 0, que es la única que tiene
                    palabraResuelta.setCharAt(2*i, letra.charAt(0));
                }
            }
            palabraOculta.setText(palabraResuelta.toString());//como palabraResuelta es un StringBuilder lo pasamos a String
        }else{
            numeroFallos++;
            dibujaImagen(numeroFallos);
        }
  
    }
    
    private void chequeaBoton(JButton boton){
        boton.setEnabled(false);//esto es para que cuando le demos al boton se deshabilite
        chequeaLetra(boton.getText());
    }
    
